package Package4;

public class ForloopExamples {
	
	// Loop is the program or control statement that used to print the program or condition more then one time
	
	
	
	
	public static void main (String [] argu) {
		
		for(int a =0; a<=10; a++) {
	//Initialization ; Condition; Increment or Decrement(){} 
			
			
			
			System.out.println(a);
		}
		
		
		for(int b=10; b>=0; b--) {
			System.out.println(b);
		}
		
		
		
	}

}
